    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String Roll = rollno.getText();
        String Physics = jTextField2.getText();
        String Maths = jTextField4.getText();
        String Em = jTextField3.getText();
        String Dbms = jTextField5.getText();
        String Os = jTextField6.getText();
  
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Misbah@7749");

            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno = '"+Roll+"'");
            
            if(rs.next()){
                
                st.executeUpdate("insert into result(rollno , physics , maths , em , dbms , os) values('"+Roll+"' , '"+Physics+"' , '"+Maths+"' , '"+Em+"' , '"+Dbms+"' , '"+Os+"')");
                JOptionPane.showMessageDialog(null , "Data Saved Succesfully");
                setVisible(false);
                 new adminHome().setVisible(true);
                
            }else{
                JOptionPane.showMessageDialog(null , "Roll Number is not registered");
            }

        }catch(Exception e){
            JOptionPane.showMessageDialog(null , e.toString());
            System.out.println(e.toString());
        }

    }//GEN-LAST:event_saveButtonActionPerformed

